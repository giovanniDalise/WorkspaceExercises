package esercizi.ereditarieta.potenziamento.rettangolo;

public class FormaGeometrica{
	protected double dimensione1;
	protected double dimensione2;
	protected double perimetro;
	protected double area;

	public FormaGeometrica(double dimensione1,double dimensione2){
		this.dimensione1=dimensione1;
		this.dimensione2=dimensione2;
	}
    public double calcolaPerimetro(double dimensione1, double dimensione2){
    	return perimetro;
    }
    public double calcolaArea(double dimensione1, double dimensione2){
    	return area;
    }
}    
